<p>
	В этом тесте вы услышите короткий ритм — последовательность звуков, подобную азбуке Морзе. Каждый
	звук сопровождается вспышкой кружка на экране.
</p>

<p>
	Ваша задача — после прослушивания воспроизвести ритм, нажимая на кружок в том же темпе. После
	завершения вы увидите сравнение оригинального ритма и вашего ответа на временной шкале.
</p>

<p>Тест помогает оценить слуховое внимание, ритмическую память и моторную координацию.</p>
